-- =====================================================================
-- UPDATE "Строка отчета" в public."FinancialData"
-- Логика маппинга управленческого P&L (на основе версии отчёта v7 + 
-- расширений по результатам анализа полной истории 2020-2026)
-- =====================================================================

UPDATE public."FinancialData"
SET "Строка отчета" = CASE

    -- ═══════════════════════════════════════════════════════════════
    -- ГЛОБАЛЬНЫЕ ПРИОРИТЕТНЫЕ ПРАВИЛА
    -- ═══════════════════════════════════════════════════════════════

    -- 1. Дивиденды_Биб / Дивиденды_Мм = аналог кэшбэка "Спасибо" (любой свод)
    WHEN LOWER(TRIM("СтатьяУровень3")) IN ('дивиденды_биб', 'дивиденды_мм')
        THEN 'Заработная плата (кэшбэк)'

    -- 2. Ввод начальных остатков — технические остатки, не P&L
    WHEN LOWER(TRIM(COALESCE("Признак",'')))     LIKE '%начальных остатков%'
      OR LOWER(TRIM(COALESCE("Категория",'')))   LIKE '%начальных остатков%'
      OR LOWER(TRIM(COALESCE("СтатьяУровень3",''))) LIKE '%начальных остатков%'
        THEN 'Не в отчёте (остатки на начало)'

    -- 3. Проекты MoneyMan / SMS — портфели цессий, логика как DCA/DP
    WHEN LOWER(TRIM(COALESCE("Проект",''))) IN ('moneyman','sms')
         AND LOWER(TRIM("СтатьяУровень3")) IN ('доход','доход от фин. деятельности','финансы')
        THEN 'Выручка DP (цессия)'
    WHEN LOWER(TRIM(COALESCE("Проект",''))) IN ('moneyman','sms')
         AND LOWER(TRIM("СтатьяУровень3")) = 'комиссия'
        THEN 'Банковские расходы'
    WHEN LOWER(TRIM(COALESCE("Проект",''))) IN ('moneyman','sms')
         AND LOWER(TRIM("СтатьяУровень3")) = 'спасибо'
        THEN 'Заработная плата (кэшбэк)'
    WHEN LOWER(TRIM(COALESCE("Проект",''))) IN ('moneyman','sms')
         AND LOWER(TRIM("СтатьяУровень3")) IN ('внутреннее перемещение','внутреннее перемещение приход','внутреннее перемещение расход')
        THEN 'Внутренние перемещения (нетто)'

    -- 4. "Разобрать" — нераспределённое (кроме явной выручки в 01/02)
    WHEN LOWER(TRIM(COALESCE("Признак",''))) = 'разобрать'
         AND LOWER(TRIM("СтатьяСвод")) NOT IN ('01. выручка профильная (+)','02. прочие доходы (+)')
        THEN 'Прочие операционные расходы'

    -- ═══════════════════════════════════════════════════════════════
    -- 01. ВЫРУЧКА ПРОФИЛЬНАЯ (+)
    -- ═══════════════════════════════════════════════════════════════
    WHEN LOWER(TRIM("СтатьяСвод")) = '01. выручка профильная (+)' THEN
        CASE
            WHEN LOWER(TRIM("СтатьяУровень3")) = 'агентское вознаграждение' THEN 'Выручка DCA (агентская)'
            WHEN LOWER(TRIM("СтатьяУровень3")) = 'поступления от должников'  THEN 'Выручка DP (цессия)'
            WHEN LOWER(TRIM("СтатьяУровень3")) IN ('прочие доходы','продажа')  THEN 'Прочая выручка (перепродажа)'
            ELSE 'Выручка DCA (агентская)'
        END

    -- ═══════════════════════════════════════════════════════════════
    -- 02. ПРОЧИЕ ДОХОДЫ (+)
    -- ═══════════════════════════════════════════════════════════════
    WHEN LOWER(TRIM("СтатьяСвод")) = '02. прочие доходы (+)' THEN
        CASE
            WHEN LOWER(TRIM("СтатьяУровень3")) = 'процент_депозит' THEN 'Депозиты'
            ELSE 'Прочая выручка (перепродажа)'
        END

    -- ═══════════════════════════════════════════════════════════════
    -- 03. РАСХОДЫ ПОРТФЕЛЬНЫЕ (-) — переменные
    -- ═══════════════════════════════════════════════════════════════
    WHEN LOWER(TRIM("СтатьяСвод")) = '03. расходы портфельные (-)' THEN
        CASE
            WHEN LOWER(TRIM(COALESCE("Признак",''))) = 'финансы' THEN 'Прочая выручка (перепродажа)'
            ELSE CASE LOWER(TRIM("СтатьяУровень3"))
                WHEN 'банкроты'                            THEN 'Агентское вознаграждение'
                WHEN 'внутреннее перемещение'              THEN 'Внутренние перемещения (нетто)'
                WHEN 'госпошлина'                          THEN 'Гос. пошлина (ВПК)'
                WHEN 'гсм'                                 THEN 'ГСМ (служба изъятия)'
                WHEN 'зп'                                  THEN 'ФОТ переменный'
                WHEN 'зп агенты'                           THEN 'ФОТ переменный'
                WHEN 'изъятие'                             THEN 'Премия за изъятие авто'
                WHEN 'информация'                          THEN 'Прочие операционные расходы'
                WHEN 'командировка'                        THEN 'Прочие операционные расходы'
                WHEN 'комиссия'                            THEN 'Банковские расходы'
                WHEN 'компенсация расходов'                THEN 'Прочие операционные расходы'
                WHEN 'нотариус'                            THEN 'Нотариус и регистрация'
                WHEN 'офисные расходы'                     THEN 'Прочие операционные расходы'
                WHEN 'поток'                               THEN 'Выручка DP (цессия)'
                WHEN 'почтовые расходы'                    THEN 'Почтовые расходы'
                WHEN 'представительские расходы, поездки' THEN 'Прочие операционные расходы'
                WHEN 'премия'                              THEN 'ФОТ переменный'
                WHEN 'прочие расходы'                      THEN 'Прочие операционные расходы'
                WHEN 'прочие доходы'                       THEN 'Прочая выручка (перепродажа)'
                WHEN 'рбр'                                 THEN 'Прочие операционные расходы'
                WHEN 'регистрация залога'                  THEN 'Нотариус и регистрация'
                WHEN 'реклама'                             THEN 'Прочие операционные расходы'
                WHEN 'ремонт'                              THEN 'Прочие операционные расходы'
                WHEN 'спасибо'                             THEN 'Заработная плата (кэшбэк)'
                WHEN 'стоянка авто'                        THEN 'Стоянка авто (служба изъятия)'
                WHEN 'судебные расходы'                    THEN 'Прочие операционные расходы'
                WHEN 'хоз расходы'                         THEN 'Прочие операционные расходы'
                WHEN 'цессия мерседес'                     THEN 'Прочие операционные расходы'
                WHEN 'штраф'                               THEN 'Штрафы'
                WHEN 'штраф за вождение'                   THEN 'Штрафы'
                WHEN 'штрафы'                              THEN 'Штрафы'
                WHEN 'эвакуатор'                           THEN 'Эвакуатор (служба изъятия)'
                WHEN 'экспертиза'                          THEN 'Прочие операционные расходы'
                WHEN 'нет статьи'                          THEN 'Прочие операционные расходы'
                WHEN 'налоги'                              THEN 'Налог на прибыль'
                WHEN 'налоги зп'                           THEN 'Гос. пошлина (ВПК)'
                WHEN 'связь'                               THEN 'Прочие операционные расходы'
                WHEN 'расходы it'                          THEN 'IT и оборудование'
                WHEN 'маяки'                               THEN 'IT и оборудование'
                WHEN 'общее'                               THEN 'Прочие операционные расходы'
                WHEN 'зенит'                               THEN 'Прочие операционные расходы'
                WHEN 'возврат ошибочных платежей'          THEN 'Прочие операционные расходы'
                WHEN 'банковское обслуживание'             THEN 'Банковские расходы'
                ELSE 'Прочие операционные расходы'
            END
        END

    -- ═══════════════════════════════════════════════════════════════
    -- 04. РАСХОДЫ ПРОЧИЕ (-) — постоянные
    -- ═══════════════════════════════════════════════════════════════
    WHEN LOWER(TRIM("СтатьяСвод")) = '04. расходы прочие (-)' THEN
        CASE
            -- финансовый блок внутри 04
            WHEN LOWER(TRIM(COALESCE("Признак",''))) = 'финансы' THEN
                CASE
                    WHEN LOWER(TRIM(COALESCE("Категория",''))) = 'займы' THEN 'Обслуживание долга (проценты)'
                    WHEN LOWER(TRIM(COALESCE("Категория",''))) LIKE '%внутреннее перемещение%' THEN 'Внутренние перемещения (нетто)'
                    WHEN LOWER(TRIM("СтатьяУровень3")) = 'налоги' THEN 'Налог на прибыль'
                    ELSE 'Внутренние перемещения (нетто)'
                END
            -- ЗП Генерального директора (Зудин С.А.) — отдельная строка, идёт в блок Бонусы
            WHEN LOWER(TRIM("СтатьяУровень3")) IN ('зп','премия')
                 AND TRIM(COALESCE("Контрагент",'')) = 'Зудин Сергей Андреевич'
                THEN 'ЗП Генерального директора'
            ELSE CASE LOWER(TRIM("СтатьяУровень3"))
                WHEN 'зп'                                  THEN 'ФОТ постоянный'
                WHEN 'премия'                              THEN 'ФОТ постоянный'
                WHEN 'налоги зп'                           THEN 'Гос. пошлина (ВПК)'
                WHEN 'налоги'                              THEN 'Налог на прибыль'
                WHEN 'прочее'                              THEN 'Прочие операционные расходы'
                WHEN 'аренда'                              THEN 'Аренда'
                WHEN 'it оборудование'                     THEN 'IT и оборудование'
                WHEN 'расходы it'                          THEN 'IT и оборудование'
                WHEN 'лицензии, софт'                      THEN 'IT и оборудование'
                WHEN 'маяки'                               THEN 'IT и оборудование'
                WHEN 'банковское обслуживание'             THEN 'Банковские расходы'
                WHEN 'комиссия'                            THEN 'Банковские расходы'
                WHEN 'гсм'                                 THEN 'ГСМ (служба изъятия)'
                WHEN 'почтовые расходы'                    THEN 'Почтовые расходы'
                WHEN 'связь'                               THEN 'Прочие операционные расходы'
                WHEN 'офисные расходы'                     THEN 'Прочие операционные расходы'
                WHEN 'представительские расходы, поездки' THEN 'Прочие операционные расходы'
                WHEN 'информация'                          THEN 'Прочие операционные расходы'
                WHEN 'командировка'                        THEN 'Прочие операционные расходы'
                WHEN 'прочие расходы'                      THEN 'Прочие операционные расходы'
                WHEN 'нет статьи'                          THEN 'Прочие операционные расходы'
                WHEN 'прочие доходы'                       THEN 'Прочая выручка (перепродажа)'
                WHEN 'штрафы, пени, неустойки'             THEN 'Штрафы'
                WHEN 'спасибо'                             THEN 'Заработная плата (кэшбэк)'
                WHEN 'стоянка авто'                        THEN 'Стоянка авто (служба изъятия)'
                WHEN 'эвакуатор'                           THEN 'Эвакуатор (служба изъятия)'
                WHEN 'изъятие'                             THEN 'Премия за изъятие авто'
                ELSE 'Прочие операционные расходы'
            END
        END

    -- ═══════════════════════════════════════════════════════════════
    -- 05. ФИНАНСЫ (+/-)
    -- ═══════════════════════════════════════════════════════════════
    WHEN LOWER(TRIM("СтатьяСвод")) = '05. финансы (+/-)' THEN
        CASE
            WHEN LOWER(TRIM("СтатьяУровень3")) = 'привлечение займов'
                THEN CASE WHEN "Сумма" >= 0 THEN 'Займ приход' ELSE 'Возврат тела займа' END
            WHEN LOWER(TRIM("СтатьяУровень3")) = 'возврат займов' THEN 'Возврат тела займа'
            WHEN LOWER(TRIM("СтатьяУровень3")) LIKE '%внутреннее перемещение%' THEN 'Внутренние перемещения (нетто)'
            WHEN LOWER(TRIM("СтатьяУровень3")) = 'финансы' THEN 'Выручка DP (цессия)'
            WHEN LOWER(TRIM(COALESCE("Категория",''))) = 'займы' AND LOWER(TRIM("СтатьяУровень3")) = 'займы' THEN 'Возврат тела займа'
            WHEN LOWER(TRIM("СтатьяУровень3")) = 'спасибо' THEN 'Заработная плата (кэшбэк)'
            WHEN LOWER(TRIM("СтатьяУровень3")) = 'комиссия' THEN 'Банковские расходы'
            WHEN LOWER(TRIM("СтатьяУровень3")) IN ('доход','доход от фин. деятельности') THEN 'Прочая выручка (перепродажа)'
            WHEN LOWER(TRIM("СтатьяУровень3")) = 'депозит' THEN 'Депозиты'
            WHEN LOWER(TRIM(COALESCE("Категория",''))) = 'прочие доходы'
                 AND LOWER(TRIM("СтатьяУровень3")) LIKE '%возврат ошибочных платежей%' THEN 'Прочая выручка (перепродажа)'
            WHEN LOWER(TRIM("СтатьяУровень3")) LIKE '%возврат ошибочных платежей%'
                 OR LOWER(TRIM("СтатьяУровень3")) IN ('нет статьи','прочие финансовые расходы') THEN 'Прочие операционные расходы'
            ELSE 'Внутренние перемещения (нетто)'
        END

    -- ═══════════════════════════════════════════════════════════════
    -- 06. ИНВЕСТИЦИИ (+/-)
    -- ═══════════════════════════════════════════════════════════════
    WHEN LOWER(TRIM("СтатьяСвод")) = '06. инвестиции (+/-)' THEN
        CASE
            WHEN LOWER(TRIM(COALESCE("Категория",''))) = 'комиссия' THEN 'Банковские расходы'
            WHEN LOWER(TRIM("СтатьяУровень3")) LIKE '%внутреннее перемещение%' THEN 'Внутренние перемещения (нетто)'
            ELSE 'Покупка портфелей (цессия)'
        END

    -- ═══════════════════════════════════════════════════════════════
    -- 07. ВНЕ БЮДЖЕТА (+/-)
    -- ═══════════════════════════════════════════════════════════════
    WHEN LOWER(TRIM("СтатьяСвод")) = '07. вне бюджета (+/-)' THEN
        CASE
            WHEN LOWER(TRIM("СтатьяУровень3")) = 'комиссия' THEN 'Банковские расходы'
            ELSE 'Прочие операционные расходы'
        END

    -- ═══════════════════════════════════════════════════════════════
    -- 08. УЧРЕДИТЕЛИ (+/-)
    -- ═══════════════════════════════════════════════════════════════
    WHEN LOWER(TRIM("СтатьяСвод")) = '08. учредители (+/-)' THEN
        CASE
            WHEN LOWER(TRIM(COALESCE("Категория",''))) = 'дивиденды'
                 OR LOWER(TRIM("СтатьяУровень3")) IN ('дивиденды','дивиденды_кап_доля','дивиденды_абр_выкуп')
                THEN 'Дивиденды учредителям'

            -- "увеличение задолженности" — лизинговые платежи за учредителей
            WHEN LOWER(TRIM(COALESCE("Категория",''))) = 'увеличение задолженности' THEN
                CASE
                    WHEN LOWER(TRIM("СтатьяУровень3")) = 'дивиденды' THEN 'Дивиденды учредителям'
                    ELSE 'Лизинг и авто'
                END

            WHEN LOWER(TRIM(COALESCE("Категория",''))) = 'нет категории' THEN
                CASE LOWER(TRIM("СтатьяУровень3"))
                    WHEN 'инвестиционные взносы'  THEN 'Займ приход'
                    WHEN 'займы'                  THEN 'Возврат тела займа'
                    WHEN 'лизинг_платежи'         THEN 'Лизинг и авто'
                    WHEN 'лизинг_расходы'         THEN 'Лизинг и авто'
                    WHEN 'штраф за вождение'      THEN 'Штрафы'
                    ELSE 'Прочие операционные расходы'
                END

            WHEN LOWER(TRIM(COALESCE("Категория",''))) = 'инвестиционные' THEN
                CASE
                    WHEN LOWER(TRIM("СтатьяУровень3")) = 'эвакуатор' THEN 'Эвакуатор (служба изъятия)'
                    ELSE 'Прочие операционные расходы'
                END

            WHEN LOWER(TRIM(COALESCE("Категория",''))) = 'учредители' THEN
                CASE
                    WHEN LOWER(TRIM("СтатьяУровень3")) IN ('лизинг','лизинг_платежи','лизинг_расходы','лизинг курцев','лизинг камиль','лизинг капустин') THEN 'Лизинг и авто'
                    WHEN LOWER(TRIM("СтатьяУровень3")) IN ('страховка','стаховка','страхование') THEN 'Лизинг и авто'
                    WHEN LOWER(TRIM("СтатьяУровень3")) = 'комиссия' THEN 'Банковские расходы'
                    WHEN LOWER(TRIM("СтатьяУровень3")) = 'штраф за вождение' THEN 'Штрафы'
                    WHEN LOWER(TRIM("СтатьяУровень3")) = 'госпошлина' THEN 'Гос. пошлина (ВПК)'
                    WHEN LOWER(TRIM("СтатьяУровень3")) = 'транспортный налог' THEN 'Гос. пошлина (ВПК)'
                    WHEN LOWER(TRIM("СтатьяУровень3")) = 'зп' THEN 'ФОТ постоянный'
                    WHEN LOWER(TRIM("СтатьяУровень3")) = 'гсм' THEN 'Лизинг и авто'
                    WHEN LOWER(TRIM("СтатьяУровень3")) IN ('внутреннее перемещение приход','внутреннее перемещение расход','технические проводки') THEN 'Внутренние перемещения (нетто)'
                    WHEN LOWER(TRIM("СтатьяУровень3")) = 'прочие доходы' THEN 'Прочая выручка (перепродажа)'
                    ELSE 'Прочие операционные расходы'
                END

            WHEN LOWER(TRIM(COALESCE("Категория",''))) = 'выплата %' THEN 'Обслуживание долга (проценты)'

            WHEN LOWER(TRIM(COALESCE("Категория",''))) = 'финансы' THEN
                CASE
                    WHEN LOWER(TRIM("СтатьяУровень3")) = 'кредит' THEN 'Займ приход'
                    WHEN LOWER(TRIM("СтатьяУровень3")) = 'технические проводки' THEN 'Внутренние перемещения (нетто)'
                    WHEN LOWER(TRIM("СтатьяУровень3")) = 'гсм' THEN 'Лизинг и авто'
                    ELSE 'Прочие операционные расходы'
                END

            WHEN LOWER(TRIM(COALESCE("Категория",''))) LIKE '%внутреннее перемещение%' THEN 'Внутренние перемещения (нетто)'

            WHEN LOWER(TRIM(COALESCE("Категория",''))) = 'прочие расходы' THEN
                CASE
                    WHEN LOWER(TRIM("СтатьяУровень3")) LIKE '%налоги%' THEN 'Гос. пошлина (ВПК)'
                    ELSE 'Прочие операционные расходы'
                END

            WHEN LOWER(TRIM(COALESCE("Категория",''))) = 'технические переносы' THEN 'Внутренние перемещения (нетто)'
            WHEN LOWER(TRIM(COALESCE("Категория",''))) = 'списание дз' THEN 'Прочие операционные расходы'
            WHEN LOWER(TRIM("СтатьяУровень3")) = '???' THEN 'Прочие операционные расходы'
            ELSE 'Прочие операционные расходы'
        END

    -- ═══════════════════════════════════════════════════════════════
    -- 09. ПЕРЕПРОДАЖА МАШИН (+/-) — целиком в одну строку
    -- ═══════════════════════════════════════════════════════════════
    WHEN LOWER(TRIM("СтатьяСвод")) = '09. перепродажа машин (+/-)' THEN 'Прочая выручка (перепродажа)'

    -- ═══════════════════════════════════════════════════════════════
    -- 10. НЕТ В СВОДЕ
    -- ═══════════════════════════════════════════════════════════════
    WHEN LOWER(TRIM("СтатьяСвод")) = '10. нет в своде' THEN
        CASE
            WHEN LOWER(TRIM(COALESCE("Признак",''))) = 'финансы' THEN
                CASE
                    WHEN LOWER(TRIM(COALESCE("Категория",''))) LIKE '%внутреннее перемещение%' THEN 'Внутренние перемещения (нетто)'
                    WHEN LOWER(TRIM(COALESCE("Категория",''))) IN ('не наше','не наше!') THEN 'Прочие операционные расходы'
                    WHEN LOWER(TRIM(COALESCE("Категория",''))) = 'прочие доходы' THEN
                        CASE
                            WHEN LOWER(TRIM("СтатьяУровень3")) = 'процент_депозит' THEN 'Депозиты'
                            ELSE 'Прочая выручка (перепродажа)'
                        END
                    WHEN LOWER(TRIM(COALESCE("Категория",''))) = 'доход' THEN
                        CASE WHEN LOWER(TRIM("СтатьяУровень3"))='спасибо' THEN 'Заработная плата (кэшбэк)' ELSE 'Прочие операционные расходы' END
                    WHEN LOWER(TRIM(COALESCE("Категория",''))) = 'moneyman' THEN
                        CASE LOWER(TRIM("СтатьяУровень3"))
                            WHEN 'спасибо' THEN 'Заработная плата (кэшбэк)'
                            WHEN 'комиссия' THEN 'Банковские расходы'
                            ELSE 'Прочие операционные расходы'
                        END
                    WHEN LOWER(TRIM(COALESCE("Категория",''))) = 'неразобранные расходы' THEN
                        CASE
                            WHEN LOWER(TRIM("СтатьяУровень3")) LIKE '%внутреннее перемещение%' THEN 'Внутренние перемещения (нетто)'
                            WHEN LOWER(TRIM("СтатьяУровень3")) = 'комиссия' THEN 'Банковские расходы'
                            WHEN LOWER(TRIM("СтатьяУровень3")) = 'прочие доходы' THEN 'Прочая выручка (перепродажа)'
                            ELSE 'Прочие операционные расходы'
                        END
                    WHEN LOWER(TRIM(COALESCE("Категория",''))) = 'учредители' THEN 'Внутренние перемещения (нетто)'
                    ELSE 'Прочие операционные расходы'
                END

            WHEN LOWER(TRIM(COALESCE("Признак",''))) = 'операционка' THEN
                CASE
                    WHEN LOWER(TRIM(COALESCE("Категория",''))) = 'выручка' THEN
                        CASE LOWER(TRIM("СтатьяУровень3"))
                            WHEN 'поступления от должников' THEN 'Выручка DP (цессия)'
                            WHEN 'эвакуатор' THEN 'Эвакуатор (служба изъятия)'
                            WHEN 'агентское вознаграждение' THEN 'Выручка DCA (агентская)'
                            ELSE 'Прочая выручка (перепродажа)'
                        END
                    WHEN LOWER(TRIM(COALESCE("Категория",''))) LIKE '%внутреннее перемещение%'
                         OR LOWER(TRIM("СтатьяУровень3")) LIKE '%внутреннее перемещение%' THEN 'Внутренние перемещения (нетто)'
                    WHEN LOWER(TRIM(COALESCE("Категория",''))) = 'финансы' THEN 'Внутренние перемещения (нетто)'
                    WHEN LOWER(TRIM(COALESCE("Категория",''))) = 'прочие доходы' THEN 'Прочая выручка (перепродажа)'
                    WHEN LOWER(TRIM(COALESCE("Категория",''))) = 'не наше' THEN 'Прочие операционные расходы'
                    WHEN LOWER(TRIM(COALESCE("Категория",''))) = 'переменные расходы' THEN
                        CASE LOWER(TRIM("СтатьяУровень3"))
                            WHEN 'стоянка авто' THEN 'Стоянка авто (служба изъятия)'
                            WHEN 'эвакуатор' THEN 'Эвакуатор (служба изъятия)'
                            WHEN 'изъятие' THEN 'Премия за изъятие авто'
                            WHEN 'госпошлина' THEN 'Гос. пошлина (ВПК)'
                            ELSE 'Прочие операционные расходы'
                        END
                    ELSE 'Прочие операционные расходы'
                END

            WHEN LOWER(TRIM(COALESCE("Признак",''))) = 'учредители' THEN
                CASE
                    WHEN LOWER(TRIM(COALESCE("Категория",''))) = 'дивиденды' THEN 'Дивиденды учредителям'
                    WHEN LOWER(TRIM(COALESCE("Категория",''))) = 'привлечение займов'
                        THEN CASE WHEN "Сумма" >= 0 THEN 'Займ приход' ELSE 'Возврат тела займа' END
                    WHEN LOWER(TRIM(COALESCE("Категория",''))) = 'погашение займа' THEN 'Возврат тела займа'
                    WHEN LOWER(TRIM(COALESCE("Категория",''))) = 'учредители' THEN
                        CASE LOWER(TRIM("СтатьяУровень3"))
                            WHEN 'лизинг_платежи' THEN 'Лизинг и авто'
                            WHEN 'штраф за вождение' THEN 'Штрафы'
                            WHEN 'комиссия' THEN 'Банковские расходы'
                            WHEN 'внутреннее перемещение расход' THEN 'Внутренние перемещения (нетто)'
                            WHEN 'прочие расходы' THEN 'Прочие операционные расходы'
                            WHEN 'госпошлина' THEN 'Гос. пошлина (ВПК)'
                            ELSE 'Прочие операционные расходы'
                        END
                    WHEN LOWER(TRIM(COALESCE("Категория",''))) LIKE '%внутреннее перемещение%' THEN 'Внутренние перемещения (нетто)'
                    WHEN LOWER(TRIM(COALESCE("Категория",''))) = 'личные расходы' THEN
                        CASE WHEN LOWER(TRIM("СтатьяУровень3")) LIKE '%внутреннее перемещение%' THEN 'Внутренние перемещения (нетто)' ELSE 'Прочие операционные расходы' END
                    WHEN LOWER(TRIM(COALESCE("Категория",''))) IN ('выплаты','расходы') THEN 'Прочие операционные расходы'
                    WHEN LOWER(TRIM(COALESCE("Категория",''))) = 'выплата %' THEN 'Обслуживание долга (проценты)'
                    ELSE 'Прочие операционные расходы'
                END

            WHEN LOWER(TRIM(COALESCE("Признак",''))) = 'вне бюджета' THEN 'Прочие операционные расходы'
            WHEN LOWER(TRIM(COALESCE("Признак",''))) = 'инвестиции' THEN 'Покупка портфелей (цессия)'
            WHEN LOWER(TRIM(COALESCE("Признак",''))) = 'технические проводки' THEN 'Внутренние перемещения (нетто)'
            WHEN LOWER(TRIM(COALESCE("Признак",''))) = 'разобрать' THEN
                CASE WHEN LOWER(TRIM(COALESCE("Категория",'')))='прочие доходы' THEN 'Прочая выручка (перепродажа)' ELSE 'Прочие операционные расходы' END
            ELSE 'Прочие операционные расходы'
        END

    -- ═══════════════════════════════════════════════════════════════
    -- ПРЕДОХРАНИТЕЛЬ
    -- ═══════════════════════════════════════════════════════════════
    ELSE 'Прочие операционные расходы'
END;


