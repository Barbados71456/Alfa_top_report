("Распределение", "Статья", "Период", "Признак", "Категория", "Проект", "Контрагент", "Тип Кошелька", "Кошелек",  "Поток", "P&L", "UE",
"СтатьяУровень0", "СтатьяУровень1", "СтатьяУровень2", "СтатьяУровень3", "СтатьяУровень4",  "СтатьяСвод", п_ф, "Контрагент_report", "Сумма", "Строка отчета")

