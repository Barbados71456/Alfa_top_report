from (SELECT 'распределение' as "Распределение", 'распределение' as "Статья", "Период", 'распределение' as "Признак", 'распределение' as "Категория", "Проект", case when "Контрагент_report"<>'Без КА' then "Контрагент" else 'Без КА' end as "Контрагент", 'распределение' as "Тип Кошелька", 'распределение' as "Кошелек",  'распределение' as "Поток", 'распределение' as "P&L", 'распределение' as "UE"
, "СтатьяУровень0", "СтатьяУровень1", "СтатьяУровень2", "СтатьяУровень3", "СтатьяУровень4", null as "Дата", "СтатьяСвод", п_ф, "Контрагент_report", "Сумма", "Строка отчета"
FROM public."rasp_FOT"

union all

SELECT 'распределение' as "Распределение", 'распределение' as "Статья", "Период", 'распределение' as "Признак", 'распределение' as "Категория", "Проект", case when "Контрагент_report"<>'Без КА' then "Контрагент" else 'Без КА' end as "Контрагент", 'распределение' as "Тип Кошелька", 'распределение' as "Кошелек",  'распределение' as "Поток", 'распределение' as "P&L", 'распределение' as "UE"
, "СтатьяУровень0", "СтатьяУровень1", "СтатьяУровень2", "СтатьяУровень3", "СтатьяУровень4", null as "Дата", "СтатьяСвод", п_ф, "Контрагент_report", "Сумма", "Строка отчета"
FROM public."rasp_Ostatki") as a

group by a."Распределение", a."Статья", a."Период", a."Признак", a."Категория", a."Проект", 
a."Контрагент", a."Тип Кошелька", a."Кошелек",  a."Поток", a."P&L", a."UE", a."СтатьяУровень0", a."СтатьяУровень1",
a."СтатьяУровень2", a."СтатьяУровень3", a."СтатьяУровень4",  a."СтатьяСвод", a.п_ф, a."Контрагент_report", a."Строка отчета"


