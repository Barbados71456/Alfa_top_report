-- раньше в том же прогоне. Здесь явно чистим строки "распределение" целиком
-- (это производная секция — пересчитывается по всей истории "до распределения"
-- каждый раз, это и есть её дизайн, см. base_driver/rasp_FOT/rasp_Ostatki).
delete from public."FinancialData" where "Распределение" = 'распределение';

